				<p>Lynchbourg Lemonade</p>
				<h2>Glassware:</h2>
				<p>Collins</p>
				<h2>Ingredients:</h2>
				<ul>
					<li>25ml Bourbon Whiskey</li>
					<li>25ml Triple Sec</li>
					<li>25ml Lemon Juice</li>
					<li>5ml Sugar Syrup</li>
					<li>Lemonade</li>
					<li>Slice of Lemon</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>Shake all ingredients apart from the lemonade in a shaker filled with ice</li> 
					<li>Strain into a collins glass filled with ice</li> 
					<li>Add lemonade to top</li>
					<li>Garnish with a slice of lemon</li>
				</ul>
				<h2>History:</h2>
				<p>Created in 1980 with with Jack Daniels, whose distillery can be found in Lynchbourg, Tennessee</p>


