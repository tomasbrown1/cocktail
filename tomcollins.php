				<h2>Name:</h2>
				<p>Tom Collins</p>
				<h2>Glassware:</h2>
				<p>Collins</p>
				<h2>Ingredients:</h2>
				<ul>
					<li>50ml Gin</li>
					<li>30ml Lemon Juice</li>
					<li>15ml Sugar Syrup</li>
					<li>Soda Water</li>
					<li>Lemon</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>Shake the gin, lemon juice and sugar syrup in a shaker filled with ice</li> 
					<li>Strain into a Collins glass filled with ice</li> 
					<li>Top up with soda</li>
					<li>Garnish with a slice of lemon</li>
				</ul>
				<h2>History:</h2>
