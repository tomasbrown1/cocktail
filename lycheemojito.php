				<p>Lychee Mojito</p>
				<h2>Glassware:</h2>
				<p>Collins</p>
				<h2>Ingredients:</h2>
				<ul>
					<li>25ml White Rum</li>
					<li>25ml Lychee Liqueur</li>
					<li>4 Wedges of Lime</li>
					<li>2 Teaspoons of Brown Sugar</li>
					<li>8 Mint Leaves</li>
					<li>Soda</li>
					<li>Crushed Ice</li>
					<li>1 Lychee</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>Muddle the limes and sugar in the bottom of the collins glass</li> 
					<li>Add the rum, lychee liqueur and mint leaves</li> 
					<li>Fill glass with crushed ice and stir vigorously</li>
					<li>Finally top up with crushed ice and soda</li>
					<li>Garnish with a lychee</li>
				</ul>
