				<h2>Glassware:</h2>
				<p>Old Fashioned</p>
				<h2>Ingredients:</h2>
				<ul>
					<li>50ml Bourbon Whiskey</li>
					<li>10ml Water</li>
					<li>2 Dashes of Bitters</li>
					<li>1 Teaspoon of Sugar</li>
					<li>Orange</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>In the bottom of an Old Fashioned glass, mix the water, bitters and sugar together using a bar spoon</li> 
					<li>Add a few cubes of ice and stir for 20 seconds</li> 
					<li>Add half of the whiskey and more ice and stir again for 20 seconds</li>
					<li>Finally add the rest of the whiskey and top up with ice</li>
					<li>Give the mix one last long stir</li>
					<li>Garnish with a slice of orange</li>
				</ul>
				<h2>History:</h2>
				<p>The Old Fashioned cocktail is such a classic that the Old Fashioned glass was named after it. The cocktail is documented as being created in 1881 in Louisville, Kentucky although similar recipes had been around since the early 1800s</p>
