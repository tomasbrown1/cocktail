				<h2>Ingredients:</h2>
				<ul>
					<li>50ml Vodka</li>
					<li>2 Tablespoons of Sugar</li>
					<li>5 Wedges of Lime</li>
					<li>Crushed Ice</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>Muddle the limes and sugar in the bottom of an Old Fashioned glass</li> 
					<li>Fill the glass with crushed ice</li> 
					<li>Stir vigoriously</li>
					<li>Add the vodka and give the drink another stir</li>
					<li>Top up with crushed ice</li>
				</ul>
				<h2>History:</h2>
