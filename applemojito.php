				<p>Apple Mojito</p>
				<h2>Glassware:</h2>
				<p>Collins</p>
				<h2>Ingredients:</h2>
				<ul>
					<li>25ml White Rum</li>
					<li>25ml Apple Liqueur</li>
					<li>4 Wedges of Lime</li>
					<li>2 Teaspoons of Brown Sugar</li>
					<li>8 Mint Leaves</li>
					<li>Apple Juice</li>
					<li>Apple Slice</li>
					<li>Crushed Ice</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>Muddle the limes and sugar in the bottom of a collins glass</li> 
					<li>Add the rum, apple liqueur and mint leaves</li> 
					<li>Fill glass with crushed ice and stir vigorously</li>
					<li>Top up with crushed ice and apple juice</li>
					<li>Garnish with a slice of apple</li>
				</ul>
