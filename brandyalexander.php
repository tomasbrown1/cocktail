				<h2>Ingredients:</h2>
				<ul>
					<li>30ml Cognac</li>
					<li>20ml Creme de Cacao (Dark)</li>
					<li>25ml Milk</li>
					<li>25ml Cream</li>
					<li>Nutmeg</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>Shake the cognac, creme de cacao, milk and cream in a shaker filled with ice</li> 
					<li>Double strain into a chilled cocktail glass</li> 
					<li>Grate some nutmeg on top for garnish</li>
				</ul>
