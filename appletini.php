				<h2>Ingredients:</h2>
				<ul>
					<li>35ml Vodka</li>
					<li>15ml Apple Liqueur</li>
					<li>25ml Apple Juice</li>
					<li>5ml Lemon Juice</li>
					<li>Apple Slice</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>Shake the vodka, apple liqueur, apple juice and lemon juice in a shaker filled with ice</li> 
					<li>Double strain into a chilled cocktail glass</li> 
					<li>Garnish with a slice of apple</li>
				</ul>
