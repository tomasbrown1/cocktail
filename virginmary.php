				<h2>Ingredients:</h2>
				<ul>
					<li>150ml Tomato Juice</li>
					<li>15ml Lemon Juice</li>
					<li>Salt and Pepper</li>
					<li>Tabasco</li>
					<li>Worcestershire Sauce</li>
					<li>Celery</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>Fill a shaker with the tomato juice, lemon juice, salt, pepper and worcestershire sauce and add tabasco as per your preference</li> 
					<li>Sway the shaker lightly from side to side, do not shake too hard</li> 
					<li>Strain into a collins glass filled with ice</li>
					<li>Garnish with a stick of celery</li>
				</ul>
