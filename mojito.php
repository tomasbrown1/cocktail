				<h2>Name:</h2>
				<p>Mojito</p>
				<h2>Glassware:</h2>
				<p>Collins</p>
				<h2>Ingredients:</h2>
				<ul>
					<li>50ml White Rum</li>
					<li>4 Wedges of Lime</li>
					<li>2 Teaspoons of Brown Sugar</li>
					<li>8 Mint Leaves</li>
					<li>Soda</li>
					<li>Crushed Ice</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>Muddle the limes and sugar in the bottom of the collins glass</li> 
					<li>Add the rum and mint leaves</li> 
					<li>Fill glass with crushed ice</li>
					<li>Stir vigorously</li>
					<li>Top up with crushed ice and soda</li>
				</ul>
				<h2>History:</h2>
				<p>Arguably the most popular cocktail in recent years. Originating in Havana. Cuba in the 16th century, it was originally created to ward off scurvy aboard Francis Drake ship</p>
