				<h2>Ingredients:</h2>
				<ul>
					<li>35ml Port</li>
					<li>15ml Brandy</li>
					<li>1 Egg Yolk</li>
					<li>Ground Nutmeg</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>Shake the port, brandy and egg yolk in a shaker filled with ice</li> 
					<li>Strain into a chilled cocktail glass</li> 
					<li>Garnish with some ground nutmeg</li>
				</ul>
				<h2>History:</h2>
				<p>This cocktail with an egg yolk included was first documented in 1862</p>


