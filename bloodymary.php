				<h2>Name:</h2>
				<p>Bloody Mary</p>
				<h2>Glassware:</h2>
				<p>Collins</p>
				<h2>Ingredients:</h2>
				<ul>
					<li>50ml Vodka</li>
					<li>100ml Tomato Juice</li>
					<li>15ml Lemon Juice</li>
					<li>Salt and Pepper</li>
					<li>Tabasco</li>
					<li>Worcestershire Sauce</li>
					<li>Celery</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>Fill a shaker with the vodka, tomato juice, lemon juice, salt, pepper and worcestershire sauce and add tabasco for spiciness</li> 
					<li>Sway the shaker lightly from side to side, do not shake hard</li> 
					<li>Strain into a collins glass filled with ice and garnish with a stick of celery</li>
				</ul>
