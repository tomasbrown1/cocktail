				<p>English Mojito</p>
				<h2>Glassware:</h2>
				<p>Collins</p>
				<h2>Ingredients:</h2>
				<ul>
					<li>50ml Gin</li>
					<li>4 Wedges of Lime</li>
					<li>2 Tablespoons of Brown Sugar</li>
					<li>8 Mint Leaves</li>
					<li>Cucumber</li>
					<li>Soda</li>
					<li>Crushed Ice</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>Muddle the limes, sugar and a couple slices of cucumber in the bottom of the collins glass</li> 
					<li>Add gin and mint leaves</li> 
					<li>Fill glass with crushed ice and stir vigrously</li>
					<li>Top up with crushed ice and soda</li>
					<li>Garnish with cucumber</li>
				</ul>
				<h2>History:</h2>
