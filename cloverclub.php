				<h2>Name:</h2>
				<p>Clover Club</p>
				<h2>Glassware:</h2>
				<p>Cocktail</p>
				<h2>Ingredients:</h2>
				<ul>
					<li>35ml Gin</li>
					<li>15ml Creme de Framboise</li>
					<li>15ml Lemon Juice</li>
					<li>1 Egg White</li>
					<li>Raspberries</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>‘Dry’ shake the egg white, gin, creme de framboise and lemon juice without ice until the contents is nice and foamy</li> 
					<li>Add ice and shake again until chilled</li> 
					<li>Double strain into a cocktail glass</li>
					<li>Garnish with a raspberry</li>
				</ul>
				<h2>History:</h2>
				<p>Clover Club is a cocktail that dates back to 1896 and is named after a men's club of the same name</p>
