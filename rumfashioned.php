				<p>Old Fashioned</p>
				<h2>Ingredients:</h2>
				<ul>
					<li>50ml Dark Rum</li>
					<li>10ml Water</li>
					<li>2 Dashes of Bitters</li>
					<li>1 Teaspoon of Sugar</li>
					<li>Orange</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>In the bottom of an Old Fashioned glass, mix the water, bitters and sugar together using a bar spoon</li> 
					<li>Add a few cubes of ice and stir for 20 seconds</li> 
					<li>Add half of the rum and more ice and stir again for 20 seconds</li>
					<li>Add the rest of the rum</li>
					<li>Top up with ice and give it one last long stir</li>
					<li>Garnish with a slice of orange</li>
				</ul>
				<h2>History:</h2>
				<p>The Rum Fashioned is a variation on the standard Old Fashioned with rum replacing whiskey</p>
