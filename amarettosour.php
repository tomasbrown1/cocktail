				<p>Amaretto Sour</p>
				<h2>Glassware:</h2>
				<p>Old Fashioned</p>
				<h2>Ingredients:</h2>
				<ul>
					<li>50ml Amaretto</li>
					<li>25ml Lemon Juice</li>
					<li>15ml Sugar Syrup</li>
					<li>1 Egg White</li>
					<li>Bitters</li>
					<li>Slices of Lemon</li>
					<li>Cherry</li>
				</ul>
				<h2>Method:</h2>
				<ul>
					<li>Whisk the egg white up in the boston with a bar spoon until it’s foamy and full of air</li> 
					<li>Add the amaretto, lemon juice, sugar syrup and bitters</li>
					<li>Shake vigorously</li> 
					<li>Strain into an Old Fashioned glass full of ice</li>
					<li>Garnish with a lemon slice and cherry</li>
				</ul>
