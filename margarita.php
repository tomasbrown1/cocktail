				<h2>Method:</h2>
				<ul>
					<li>Run a lime around the rim of a chilled cocktail glass and dip the glass into a bowl of salt so it sticks to the rim</li> 
					<li>Shake the Tequila, Triple Sec and Lime Juice in a shaker filled with ice</li> 
					<li>Double strain the mix into the cocktail glass</li>
					<li>Garnish with a lime on the rim</li>
				</ul>
